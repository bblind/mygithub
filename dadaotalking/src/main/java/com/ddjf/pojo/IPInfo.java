package com.ddjf.pojo;

import java.text.SimpleDateFormat;
import java.util.Date;

/**
 * 
 * 构建组：大道金服科技部
 * 作者:caihewei
 * 日期:2017年8月3日下午3:03:52
 * 功能说明：
 * 
 */
public class IPInfo {

	private int id; 
	private int uId;
	private String ip;
	private String location;
	private String date = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
	
	/**
	 * 获取属性方法
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * 设置属性方法
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * 获取属性方法
	 * @return the u_id
	 */
	public int getU_id() {
		return uId;
	}
	/**
	 * 设置属性方法
	 * @param u_id the u_id to set
	 */
	public void setU_id(int uId) {
		this.uId = uId;
	}
	/**
	 * 获取属性方法
	 * @return the ip
	 */
	public String getIp() {
		return ip;
	}
	/**
	 * 设置属性方法
	 * @param ip the ip to set
	 */
	public void setIp(String ip) {
		this.ip = ip;
	}
	/**
	 * 获取属性方法
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}
	/**
	 * 设置属性方法
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}
	/**
	 * 获取属性方法
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	
}
